    private void plotLines(Plot3DPanel plot) {
        double[][] mat = new double[lines.size() * 2][3];
        for (int i = 1; i <= lines.size(); i++) {

            mat[i * 2 - 2][0] = lines.get(i - 1).P1().X();
            mat[i * 2 - 2][1] = lines.get(i - 1).P1().Y();
            mat[i * 2 - 2][2] = lines.get(i - 1).P1().Z();

            mat[i * 2 - 1][0] = lines.get(i - 1).P2().X();
            mat[i * 2 - 1][1] = lines.get(i - 1).P2().Y();
            mat[i * 2 - 1][2] = lines.get(i - 1).P2().Z();
        }

        this.addToLog("Punctul de coord X=1, Y=2, Y=3 fata de dreapta " + lines.get(0).ToStringL());
        if (!lines.get(0).continePunct(new Point(++hold, 1, 2, 3))) {
            this.addToLog("Nu apartine");
        } else {
            this.addToLog("Apartine");
        }
        plot.addLinePlot("G3", Color.RED, mat);


    }

