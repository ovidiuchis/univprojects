    public Plot3DPanel plotPoints() {

        Plot3DPanel plot = new Plot3DPanel();
        if (this.points.size() > 0) {
            double[][] mat = new double[points.size()][3];
            for (int i = 0; i < points.size(); i++) {
                mat[i][0] = points.get(i).X();
                mat[i][1] = points.get(i).Y();
                mat[i][2] = points.get(i).Z();
            }

            plot.addScatterPlot("G4", Color.GREEN, mat);
        }
        return plot;

    }

