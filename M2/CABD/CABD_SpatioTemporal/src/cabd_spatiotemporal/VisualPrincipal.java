    private Plot3DPanel PlotIstorie(String id) {
        Plot3DPanel plot = new Plot3DPanel();
        if (this.istoric.size() > 0) {
            double[][] mat = new double[istoric.size()+1][3];
            for (int i = 0; i < istoric.size(); i++) {
                mat[i][0] = istoric.get(i).X();
                mat[i][1] = istoric.get(i).Y();
                mat[i][2] = istoric.get(i).Z();
            }
            for (int k=0;k<points.size();k++){
                if (points.get(k).ID().equals(id)){
                    mat[istoric.size()-1][0]= points.get(k).X();
                    mat[istoric.size()-1][0]= points.get(k).Y();
                    mat[istoric.size()-1][0]= points.get(k).Z();
                }
            } 
            plot.addScatterPlot("G4", Color.GREEN, mat);
            plot.addLinePlot("G4", Color.BLACK, mat);
        }
        return plot;
    }

